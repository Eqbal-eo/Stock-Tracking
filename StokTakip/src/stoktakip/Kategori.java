    private void jButtonGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuncelleActionPerformed
        int secilenSatir = jTableKategori.getSelectedRow();
        if (secilenSatir == -1) {
            JOptionPane.showMessageDialog(null, "Lutfen bir secim yapiniz!");
        } else {
            String ID = jTableKategori.getValueAt(secilenSatir, 0).toString();
            String KATEGORI = jTextField2.getText();

            String sorgu = "UPDATE  KATEGORILER SET "
                    + "KATEGORI = '" + KATEGORI + "'"
                    + "WHERE ID = " + ID;
            try {
                Connection con = DriverManager.getConnection(adres, kullanici, sifre);
                Statement st = con.createStatement();
                st.executeUpdate(sorgu);

                con.close();
                TabloDoldur();
            } catch (SQLException ex) {
                jLabel6.setText("Guncellenmedi");
            }
            jLabel4.setText("Guncelleme Başarıyla Tamamlandı.");
        }
    }//GEN-LAST:event_jButtonGuncelleActionPerformed

    private void jButtonSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSilActionPerformed
        int secilenSatir = jTableKategori.getSelectedRow();
        if (secilenSatir == -1) {
            JOptionPane.showMessageDialog(null, "Lutfen bir secim yapiniz!");
        } else {
            int onay = JOptionPane.showConfirmDialog(null, "Emin misiniz?");
            if (onay == 0) {
                String ID = jTableKategori.getValueAt(secilenSatir, 0).toString();

                try {
                    Connection con = DriverManager.getConnection(adres, kullanici, sifre);
                    Statement sorgu = con.createStatement();
                    sorgu.executeUpdate("DELETE FROM KATEGORILER WHERE ID =" + ID);
                    con.close();
                    TabloDoldur();
                } catch (SQLException ex) {
                    Logger.getLogger(Personel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }    }//GEN-LAST:event_jButtonSilActionPerformed

