    private void TabloDoldur() {
        jTableKategori.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "ID", "KATEGORI"
                }
        ) {
            Class[] types = new Class[]{
                java.lang.Integer.class, java.lang.String.class
            };

            public Class getColumnClass(int columnIndex) {
                return types[columnIndex];
            }
        });
        try {
            Connection con = DriverManager.getConnection(adres, kullanici, sifre);
            Statement sorgu = con.createStatement();
            ResultSet sonuc = sorgu.executeQuery("SELECT * FROM KATEGORILER");
            int sutun = sonuc.getMetaData().getColumnCount();
            while (sonuc.next()) {
                Object[] satir = new Object[sutun];
                for (int i = 1; i <= sutun; i++) {
                    satir[i - 1] = sonuc.getObject(i);
                }
                ((DefaultTableModel) jTableKategori.getModel()).insertRow(sonuc.getRow() - 1, satir);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Bağlanti Hatası!");
        }
    }

